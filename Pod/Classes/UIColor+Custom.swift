import Foundation
import UIKit

extension UIColor {
    
    static func greyBorderColor() -> UIColor {
        return UIColor(white: 0.8, alpha: 1)
    }
    
    static func blueNavigationBarColor() -> UIColor {
        return UIColor(red: 0.18, green: 0.533, blue: 0.988, alpha: 1)
    }
}
